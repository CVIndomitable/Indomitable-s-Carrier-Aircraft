✅ 测试 7：边缘情况
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

测试A：没有锁定目标
1. 清除目标（重新加载世界或等待）
2. 直接主手右键召唤
预期：□ 飞机在头顶盘旋，不飞向任何地方

测试B：玩家移动
1. 召唤飞机后立即快速飞走（创造模式飞行）
2. 观察飞机行为
预期：□ 飞机仍飞向锁定的目标坐标（不跟随玩家）

测试C：目标在水中
1. 锁定水面或水下位置
2. 召唤飞机
预期：□ 炸弹在水中爆炸，水可能减弱爆炸

测试D：清除飞机
命令: /kill @e[type=indomitablecarrieraircraft:bomber_aircraft]
预期：□ 所有飞机立即消失

