游戏已启动！现在开始测试...

━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
📋 测试前准备
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

1. 确认游戏模式
   命令: /gamemode creative
   或按 F3+F4 切换到创造模式

2. 飞到空旷地带
   - 找一片平坦区域（便于观察飞行轨迹）
   - 建议在海平面以上 (Y > 70)

3. 打开F3调试信息
   - 按 F3 查看坐标和实体数量
   - 按 F3+B 显示碰撞箱（可选）

━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
✅ 测试 1：获取物品
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

步骤：
1. 按 E 键打开物品栏
2. 查找"不挠的舰载机"标签页（图标应该是召唤器）
3. 拿取"水平轰炸机召唤器"到快捷栏

验证：
□ 标签页存在且命名正确
□ 物品名称显示为"水平轰炸机召唤器"（中文）或"Bomber Aircraft Spawner"（英文）
□ 物品可以正常拿取

━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
✅ 测试 2：锁定目标
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

步骤：
1. 召唤器放到主手
2. 按 F 键切换到副手
3. 看向地面上一个明显的方块（例如泥土、石头）
4. 按鼠标右键

预期结果：
□ 聊天栏显示: "§e已锁定目标: (X, Y, Z)"
□ 坐标与你看向的位置大致相符

测试边缘情况：
5. 看向天空（超过200格）
6. 按鼠标右键

预期结果：
□ 聊天栏显示: "§c未找到目标"

━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
✅ 测试 3：召唤飞机
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

步骤：
1. 确保已锁定目标（测试2完成）
2. 召唤器切换回主手
3. 按鼠标右键
4. 立即抬头看向天空

预期结果：
□ 聊天栏显示: "§a已召唤水平轰炸机"
□ 头顶约20格高度出现飞机实体（可能只看到阴影）
□ 按 F3 后，实体数量增加1

调试提示：
- 如果看不到飞机，按 F3+B 显示碰撞箱
- 飞机的碰撞箱尺寸：2格宽 × 1格高

━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
✅ 测试 4：观察飞行行为
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

步骤：
1. 按 F5 切换到第三人称视角
2. 观察飞机行为（保持静止，不要移动玩家）

预期行为序列：

阶段1：STANDBY（待命）
□ 飞机在玩家头顶盘旋
□ 保持约20格高度
□ 持续时间：短暂（有目标会立即进入下一阶段）

阶段2：APPROACH（赶赴）
□ 飞机开始朝锁定目标飞去
□ 飞行速度：约0.5 blocks/tick（每秒10格）
□ 目标高度：80格
□ 飞机朝向目标方向

阶段3：DROPPING（投弹）
□ 飞机到达目标上方（80格高度）
□ 开始盘旋并投弹
□ 投弹间隔：1秒
□ 总投弹数：6发

阶段4：RETURNING（返航）
□ 6发弹药耗尽后
□ 飞机飞回玩家位置
□ 到达玩家头顶后消失

计时测试：
- 从召唤到开始投弹：取决于距离
- 投弹持续时间：约6秒（6发 × 1秒）
- 总任务时间：预计10-30秒（取决于目标距离）

━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
✅ 测试 5：炸弹物理
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

观察要点：

投下瞬间：
□ 炸弹从飞机正下方1格处生成
□ 炸弹继承飞机的水平速度
□ 炸弹显示为TNT物品模型

下落过程：
□ 炸弹有明显的自由落体运动
□ 下落速度逐渐加快（重力加速）
□ 轻微的水平减速（空气阻力）

爆炸时刻：
□ 炸弹接触地面时爆炸
□ 爆炸半径约4格
□ 产生TNT级别的破坏
□ 对附近生物造成伤害

测试精度：
1. 记录锁定目标坐标 (X1, Y1, Z1)
2. 观察炸弹实际落点
3. 计算偏差距离

预期精度：
- 水平偏差：< 5格（自由落体不精确）
- 适用场景：大面积轰炸，不适合精确打击

