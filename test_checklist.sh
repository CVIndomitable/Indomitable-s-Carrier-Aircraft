✅ 测试 6：多飞机测试
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

步骤：
1. 重新锁定同一个目标
2. 连续右键召唤3-5架飞机
3. 观察性能和行为

验证：
□ 多架飞机可以同时存在
□ 所有飞机飞向同一目标
□ FPS没有明显下降（按 F3 查看帧率）
□ 飞机之间不会碰撞

性能测试：
- 召唤前 FPS: _______
- 5架飞机 FPS: _______
- FPS 下降: _______% (预期 < 20%)

